from brc.rounding import java_round
import pytest


@pytest.mark.parametrize(
    "original, rounded",
    [
        (1.5, 2.0),
        (-1.5, -1.0),
        (2.5, 3.0),
        (-2.5, -2.0),
        (75.312, 75.0),
        (-12.097, -12.0),
        (54.893, 55.0),
        (-82.645, -83.0),
        (39.781, 40.0),
        (-66.227, -66.0),
        (88.506, 89.0),
        (20.399, 20.0),
        (15.127, 15.0),
        (-45.884, -46.0),
        (92.75, 93.0),
        (-31.419, -31.0),
        (63.268, 63.0),
        (-96.922, -97.0),
        (-8.556, -9.0),
        (5.795, 6.0),
        (69.332, 69.0),
        (-53.46, -53.0),
        (-71.139, -71.0),
        (82.168, 82.0),
    ],
)
def test_java_round(original: float, rounded: float):
    assert java_round(original) == rounded
